package edu.shiep.library.vo;
import java.util.Date;
public class Ordered {
	private int mid;
	private int pno;
	private int ono;
	private float ototal;
	private Date odate;
	private String opeople;
	private int ocount;
	private String otel;
	public int getMid() {
		return mid;
	}
	public void setMid(int mid) {
		this.mid = mid;
	}
	public int getPno() {
		return pno;
	}
	public void setPno(int pno) {
		this.pno = pno;
	}
	public int getOno() {
		return ono;
	}
	public void setOno(int ono) {
		this.ono = ono;
	}
	public float getOtotal() {
		return ototal;
	}
	public void setOtotal(float ototal) {
		this.ototal = ototal;
	}
	public Date getOdate() {
		return odate;
	}
	public void setOdate(Date odate) {
		this.odate = odate;
	}
	public String getOpeople() {
		return opeople;
	}
	public void setOpeople(String opeople) {
		this.opeople = opeople;
	}
	public int getOcount() {
		return ocount;
	}
	public void setOcount(int ocount) {
		this.ocount = ocount;
	}
	public String getOtel() {
		return otel;
	}
	public void setOtel(String otel) {
		this.otel = otel;
	}
	
	
}